/*
 
 Print following structure in Java using loops?
*
* *
* * *
* * * *
* * * * *

 */
public class starsStructure2 {

	public static void main(String[] args) {
		int a, s;
		for(a = 0; a < 5; a++) {
			for(s = 0; s <= a; s++) {
				System.out.print(" * ");
			}
			System.out.println();
		}
		

	}

}
